package fundamentos;

public class Inferencia {
    public static void main(String[] args) {
        double a = 4.5;
        System.out.println(a);

        var b = 6.5;
        System.out.println(b);

        var c = "texto";
        System.out.println(c);

        double d;
        d = 8.5;
        System.out.println(d);
    }
}
