package fundamentos;

public class JREvsJDK {
    public static void main(String[] args) {
        /*
        * JRE.
        * utilizado para executar um codigo compilado em java, que são os .class.
        * JRE quer dizer Java Runtime Env.
        *
        * JDK
        * É usado para o ambiente de desenvolvimento, compilando os arquivos .java, que são os bytecode.
        * JDK quer dizer Java development Kit.
        * */


        /*
        * JVM
        * Executar o mesmo código em diferentes hardwares, que pode ser PC, Apple, Android ou uma geladeira por exemplo.
        * A JVM pega o bytecode compilado polo JDK e executa, independentemente do tipo de hardware.
        * O única requisito é ter o JRE para o hardware correto;
        *
        * */
    }
}
